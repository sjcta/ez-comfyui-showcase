def _coerce_evidence_record(value: Any, fallback_evidence: str = "", fallback_confidence: float = 0.8) -> dict[str, Any]:
    if isinstance(value, dict) and any(key in value for key in ("value", "evidence", "confidence", "allow_positive")):
        record = dict(value)
        record.setdefault("evidence", fallback_evidence or "系统归一化证据")
        record.setdefault("confidence", fallback_confidence)
        record.setdefault("allow_positive", True)
        return record
    return {
        "value": value,
        "evidence": fallback_evidence or "系统归一化证据",
        "confidence": fallback_confidence,
        "allow_positive": True,
    }


def _normalize_visual_evidence(raw_evidence: Any) -> dict[str, Any] | None:
    """Normalize loose model evidence into the keyed evidence table used by quality gates."""
    if isinstance(raw_evidence, dict):
        if any(key in raw_evidence for key in REQUIRED_EVIDENCE_KEYS):
            normalized: dict[str, Any] = {}
            for key in REQUIRED_EVIDENCE_KEYS:
                if key in raw_evidence:
                    normalized[key] = _coerce_evidence_record(raw_evidence[key], key)
            return normalized or None
        value = raw_evidence.get("value")
        if isinstance(value, dict):
            return _normalize_visual_evidence([raw_evidence])
        return None

    if not isinstance(raw_evidence, list):
        return None

    normalized: dict[str, Any] = {}
    for item in raw_evidence:
        if not isinstance(item, dict):
            continue
        confidence = item.get("confidence")
        try:
            fallback_confidence = float(confidence)
        except (TypeError, ValueError):
            fallback_confidence = 0.8
        fallback_evidence = str(item.get("evidence") or "模型宽松证据").strip()
        value = item.get("value")
        if isinstance(value, dict):
            for key in REQUIRED_EVIDENCE_KEYS:
                if key in value and key not in normalized:
                    normalized[key] = _coerce_evidence_record(
                        value[key],
                        fallback_evidence or key,
                        fallback_confidence,
                    )
            continue
        evidence_key = str(item.get("key") or item.get("id") or item.get("field") or item.get("evidence") or "").strip()
        if evidence_key in REQUIRED_EVIDENCE_KEYS and evidence_key not in normalized:
            normalized[evidence_key] = _coerce_evidence_record(
                value,
                fallback_evidence or evidence_key,
                fallback_confidence,
            )
    return normalized or None


def _evidence_value_text(evidence_item: Any) -> str:
    if isinstance(evidence_item, dict):
        return "，".join(_iter_grouped_prompt_strings(evidence_item.get("value")))
    return "，".join(_iter_grouped_prompt_strings(evidence_item))


def _coerce_bool(value: Any, default: bool = True) -> bool:
    if isinstance(value, bool):
        return value
    if value is None:
        return default
    text = str(value).strip().lower()
    if text in {"false", "0", "no", "否", "不进入", "不允许"}:
        return False
    if text in {"true", "1", "yes", "是", "进入", "允许"}:
        return True
    return default


def _normalize_fact_cards(raw_cards: Any, *, limit: int = 0) -> list[dict[str, Any]]:
    if isinstance(raw_cards, dict):
        raw_cards = raw_cards.get("items") or raw_cards.get("facts") or raw_cards.get("fact_cards") or []
    if not isinstance(raw_cards, list):
        return []
    cards: list[dict[str, Any]] = []
    for item in raw_cards:
        if not isinstance(item, dict):
            continue
        dimension = str(
            item.get("dimension")
            or item.get("维度")
            or item.get("category")
            or item.get("label")
            or item.get("expert")
            or ""
        ).strip()
        content = _sanitize_prompt_value_text(
            item.get("visible_content")
            or item.get("可见内容")
            or item.get("content")
            or item.get("value")
            or item.get("fact")
            or ""
        )
        location = _sanitize_prompt_value_text(item.get("location") or item.get("画面位置") or item.get("position") or "")
        evidence = _sanitize_prompt_value_text(item.get("evidence") or item.get("证据") or item.get("basis") or "")
        if not content:
            continue
        try:
            confidence = float(item.get("confidence", item.get("置信度", 0.8)))
        except (TypeError, ValueError):
            confidence = 0.8
        confidence = max(0.0, min(confidence, 1.0))
        card = {
            "dimension": dimension or "图像事实",
            "visible_content": content,
            "location": location,
            "evidence": evidence,
            "confidence": confidence,
            "allow_positive": _coerce_bool(item.get("allow_positive", item.get("进入正向", True)), True),
        }
        cards.append(card)
        if limit and len(cards) >= limit:
            break
    return cards


def _expert_results_from_fact_cards(
    fact_cards: list[dict[str, Any]],
    *,
    existing_ids: set[str] | None = None,
) -> list[dict[str, Any]]:
    existing = set(existing_ids or set())
    grouped: dict[str, list[dict[str, Any]]] = {}
    for card in fact_cards or []:
        if not isinstance(card, dict) or not card.get("allow_positive", True):
            continue
        dimension = str(card.get("dimension") or "").strip()
        expert_id = _expert_id_from_observation_key(dimension) or dimension or "image_specific_detail"
        if expert_id in existing:
            continue
        grouped.setdefault(expert_id, []).append(card)
    results: list[dict[str, Any]] = []
    spec_by_id = _expert_spec_by_id()
    for expert_id, cards in grouped.items():
        snippets = []
        for card in cards[:6]:
            location = str(card.get("location") or "").strip()
            content = str(card.get("visible_content") or "").strip()
            evidence = str(card.get("evidence") or "").strip()
            text = content
            if location:
                text = f"{location}：{text}"
            if evidence:
                text = f"{text}（依据：{evidence}）"
            snippets.append(text)
        merged = _sanitize_prompt_value_text("，".join(_dedupe_prompt_items(snippets)))
        if not merged:
            continue
        spec = spec_by_id.get(expert_id)
        results.append(
            _clamp_expert_result_text(
                {
                    "id": expert_id,
                    "label": spec.get("label") if spec else str(cards[0].get("dimension") or "事实卡补充"),
                    "summary": merged,
                    "fields": {"事实卡补充": merged},
                    "observations": [merged],
                    "uncertain": [],
                    "negative_constraints": [],
                    "confidence": max(float(card.get("confidence") or 0.8) for card in cards),
                    "raw": json.dumps(cards, ensure_ascii=False),
                    "from_fact_cards": True,
                }
            )
        )
    return results


