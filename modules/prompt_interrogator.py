def _strip_ellipsis_artifacts(value: Any) -> Any:
    if isinstance(value, dict):
        cleaned: dict[str, Any] = {}
        for key, nested in value.items():
            cleaned_value = _strip_ellipsis_artifacts(nested)
            cleaned[str(key)] = cleaned_value
        return cleaned
    if isinstance(value, list):
        return [_strip_ellipsis_artifacts(item) for item in value]
    if not isinstance(value, str):
        return value
    text = re.sub(r"(?:\.{3,}|…+)", "", value)
    text = re.sub(r"\s+([，,。；;：:])", r"\1", text)
    text = re.sub(r"([，,；;：:]){2,}", r"\1", text)
    return text.strip()


def _clip_expert_text(value: Any, limit: int = 360, *, marker: str = "…") -> str:
    text = str(value or "").strip()
    if len(text) <= limit:
        return text
    if not marker:
        return text[: max(0, limit)].rstrip()
    return text[: max(0, limit - len(marker))].rstrip() + marker


def _consume_text_budget(value: Any, remaining: int, *, marker: str = "…") -> tuple[str, int]:
    text = str(value or "").strip()
    if not text or remaining <= 0:
        return "", max(0, remaining)
    if len(text) <= remaining:
        return text, remaining - len(text)
    if not marker:
        return text[:remaining].rstrip(), 0
    if remaining <= len(marker):
        return marker[:remaining], 0
    return text[: remaining - len(marker)].rstrip() + marker, 0


def _clamp_expert_result_text(item: dict[str, Any], max_chars: int = 0) -> dict[str, Any]:
    """Optionally limit one expert; default preserves full dimension detail."""
    clamped = dict(item)
    if not max_chars or int(max_chars) <= 0:
        return clamped
    remaining = max(0, int(max_chars))
    summary, remaining = _consume_text_budget(clamped.get("summary"), min(remaining, 45))
    clamped["summary"] = summary
    remaining = max(0, int(max_chars) - len(summary))

    observations: list[str] = []
    for value in clamped.get("observations") or []:
        clipped, remaining = _consume_text_budget(value, remaining)
        if clipped:
            observations.append(clipped)
        if remaining <= 0:
            break
    clamped["observations"] = observations

    fields = clamped.get("fields") if isinstance(clamped.get("fields"), dict) else {}
    limited_fields: dict[str, str] = {}
    for key, value in fields.items():
        clipped, remaining = _consume_text_budget(value, remaining)
        if clipped:
            limited_fields[str(key)] = clipped
        if remaining <= 0:
            break
    clamped["fields"] = limited_fields

    clamped["uncertain"] = [_clip_expert_text(value, 60) for value in (clamped.get("uncertain") or [])[:2]]
    clamped["negative_constraints"] = [_clip_expert_text(value, 40) for value in (clamped.get("negative_constraints") or [])[:2]]
    return clamped


def _clean_expert_team_visible_result(item: dict[str, Any]) -> dict[str, Any]:
    cleaned = _strip_ellipsis_artifacts(item)
    if not isinstance(cleaned, dict):
        return item
    summary = _sanitize_prompt_value_text(cleaned.get("summary") or "")
    fields = cleaned.get("fields") if isinstance(cleaned.get("fields"), dict) else {}
    observations = cleaned.get("observations") if isinstance(cleaned.get("observations"), list) else []
    if fields:
        merged_fields = "，".join(_dedupe_prompt_items(_iter_grouped_prompt_strings(fields)))
        if len(merged_fields) > len(summary):
            summary = merged_fields
    elif observations:
        merged_observations = "，".join(_dedupe_prompt_items(_iter_grouped_prompt_strings(observations)))
        if len(merged_observations) > len(summary):
            summary = merged_observations
    cleaned["summary"] = summary
    return cleaned


