def _local_expert_detail_failure(spec: dict[str, str], result: dict[str, Any]) -> str:
    expert_id = str(spec.get("id") or "").strip()
    if expert_id not in COORDINATE_REQUIRED_EXPERT_IDS:
        return ""
    text = "，".join(
        _iter_grouped_prompt_strings(
            [result.get("summary"), result.get("fields"), result.get("observations")]
        )
    )
    if not COORDINATE_DETAIL_RE.search(text):
        return "缺少可进入最终提示词的自然画面坐标、前景/背景关系或时钟方向"
    if expert_id in {"body_pose", "expression_language"} and not TERMINAL_HEAD_DETAIL_RE.search(text):
        return "缺少头部末梢节点：鼻尖方向、下巴关系、视线落点、脸部可见比例或发丝遮挡"
    return ""


def _expert_retry_instruction(review_report: dict[str, Any], expert_id: str) -> str:
    for review in review_report.get("reviews") or []:
        if str(review.get("id") or "") == expert_id:
            parts = []
            if review.get("missing"):
                parts.append("缺少：" + "；".join(review.get("missing") or []))
            if review.get("unsupported"):
                parts.append("不属实/越界：" + "；".join(review.get("unsupported") or []))
            if review.get("retry_instruction"):
                parts.append("重写要求：" + str(review.get("retry_instruction")))
            return "；".join(parts)
    return ""


