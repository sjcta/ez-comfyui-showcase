def run_image_interrogator(
    image_filename: str,
    base_url: str,
    comfyui_post: Callable[[str, dict, str | None], dict[str, Any]],
    comfyui_get: Callable[[str, str | None], dict[str, Any]],
    timeout: float = 180.0,
    poll_interval: float = 1.0,
) -> dict[str, Any]:
    """Submit the interrogation workflow to ComfyUI and return prompt text."""
    workflow = build_image_interrogate_workflow(image_filename)
    response = comfyui_post(
        "/prompt",
        {"prompt": copy.deepcopy(workflow), "client_id": f"ez-img-prompt-{uuid.uuid4().hex}"},
        base_url,
    )
    prompt_id = str(response.get("prompt_id") or "")
    if not prompt_id:
        raise RuntimeError("ComfyUI did not return prompt_id for image interrogation")

    deadline = time.time() + float(timeout or 180.0)
    last_poll_error: Exception | None = None
    while time.time() < deadline:
        try:
            history = comfyui_get(f"/history/{prompt_id}", base_url)
            last_poll_error = None
        except Exception as e:
            last_poll_error = e
            time.sleep(max(0.1, float(poll_interval or 1.0)))
            continue
        if isinstance(history, dict) and prompt_id in history:
            entry = history[prompt_id]
            status = entry.get("status", {}) if isinstance(entry, dict) else {}
            if status.get("completed", False):
                result = extract_interrogate_result(entry)
                if result["prompt"]:
                    return {"ok": True, "provider": _provider_for_result(result), "prompt_id": prompt_id, **result}
                raise RuntimeError("ComfyUI image interrogation completed without text output")
            if status.get("status_str") == "error":
                messages = status.get("messages", [])
                raise RuntimeError(str(messages)[:300] if messages else "ComfyUI image interrogation failed")
        time.sleep(max(0.1, float(poll_interval or 1.0)))
    raise TimeoutError("Image interrogation timed out")


def _extract_show_text_output(entry: dict[str, Any], node_id: str) -> str:
    outputs = entry.get("outputs", {}) if isinstance(entry, dict) else {}
    return _text_output(outputs, node_id)


def run_qwen_vqa_image_prompt(
    image_filename: str,
    prompt_text: str,
    base_url: str,
    comfyui_post: Callable[[str, dict, str | None], dict[str, Any]],
    comfyui_get: Callable[[str, str | None], dict[str, Any]],
    timeout: float = 180.0,
    poll_interval: float = 1.0,
    *,
    model: str = "Qwen3-VL-4B-Instruct",
    max_new_tokens: int = 2048,
    temperature: float = 0.1,
) -> str:
    """Run Qwen3 VQA with caller-provided text and return the raw text output."""
    workflow = build_qwen_vqa_prompt_workflow(
        image_filename,
        prompt_text,
        model=model,
        max_new_tokens=max_new_tokens,
        temperature=temperature,
    )
    response = comfyui_post(
        "/prompt",
        {"prompt": copy.deepcopy(workflow), "client_id": f"ez-qwen-vqa-{uuid.uuid4().hex}"},
        base_url,
    )
    prompt_id = str(response.get("prompt_id") or "")
    if not prompt_id:
        raise RuntimeError("ComfyUI did not return prompt_id for Qwen VQA")

    deadline = time.time() + float(timeout or 180.0)
    last_poll_error: Exception | None = None
    while time.time() < deadline:
        try:
            history = comfyui_get(f"/history/{prompt_id}", base_url)
            last_poll_error = None
        except Exception as e:
            last_poll_error = e
            time.sleep(max(0.1, float(poll_interval or 1.0)))
            continue
        if isinstance(history, dict) and prompt_id in history:
            entry = history[prompt_id]
            status = entry.get("status", {}) if isinstance(entry, dict) else {}
            if status.get("completed", False):
                text = _extract_show_text_output(entry, "4")
                if text:
                    return text
                raise RuntimeError("Qwen VQA completed without text output")
            if status.get("status_str") == "error":
                messages = status.get("messages", [])
                raise RuntimeError(str(messages)[:300] if messages else "Qwen VQA failed")
        time.sleep(max(0.1, float(poll_interval or 1.0)))
    suffix = f": {last_poll_error}" if last_poll_error else ""
    raise TimeoutError(f"Qwen VQA timed out{suffix}")
