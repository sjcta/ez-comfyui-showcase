def _repair_truncated_json(text: str) -> str:
    """Best-effort close for local VLM JSON that is cut off at the final braces."""
    raw = str(text or "").strip()
    if not raw.startswith("{"):
        start = raw.find("{")
        if start < 0:
            return raw
        raw = raw[start:]
    stack: list[str] = []
    in_string = False
    escape = False
    for ch in raw:
        if in_string:
            if escape:
                escape = False
            elif ch == "\\":
                escape = True
            elif ch == '"':
                in_string = False
            continue
        if ch == '"':
            in_string = True
        elif ch in "{[":
            stack.append("}" if ch == "{" else "]")
        elif ch in "}]":
            if stack and stack[-1] == ch:
                stack.pop()
    if in_string:
        raw += '"'
    if stack:
        raw += "".join(reversed(stack))
    return raw


