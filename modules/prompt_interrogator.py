def _expert_observation_from_markdown(markdown: str, spec: dict[str, str]) -> dict[str, Any]:
    """Parse a compact Markdown expert note into the normal expert result shape."""
    summary = ""
    fields: dict[str, str] = {}
    observations: list[str] = []
    negative: list[str] = []
    uncertain: list[str] = []
    confidence = 0.8
    for raw_line in str(markdown or "").splitlines():
        line = raw_line.strip().lstrip("-*").strip()
        if not line or line.startswith("#"):
            continue
        match = re.match(r"^([^:：]{1,16})\s*[:：]\s*(.+)$", line)
        if not match:
            observations.append(line)
            continue
        key = match.group(1).strip()
        value = match.group(2).strip()
        if key in {"摘要", "summary", "Summary"}:
            summary = value
            continue
        if key in {"负面", "负面提示词", "规避", "negative", "Negative"}:
            cleaned_negative = _clean_negative_prompt_text(value)
            negative.extend(
                item.strip()
                for item in re.split(r"[,，、/]+", cleaned_negative)
                if item.strip()
            )
            continue
        if key in {"断言", "claim", "Claim", "观察", "事实"}:
            observations.append(value)
            continue
        if key in {"不确定", "风险", "uncertain", "Uncertain"}:
            uncertain.append(value)
            continue
        if key in {"置信度", "confidence", "Confidence"}:
            try:
                confidence = max(0.0, min(float(value), 1.0))
            except Exception:
                confidence = 0.8
            continue
        normalized_key = key
        if normalized_key.startswith("字段/"):
            normalized_key = normalized_key.split("/", 1)[1].strip() or "字段"
        fields[normalized_key] = value
    summary = summary or "，".join(_dedupe_prompt_items(_iter_grouped_prompt_strings(fields))) or "，".join(observations)
    return _clamp_expert_result_text(
        {
            "id": spec["id"],
            "label": spec["label"],
            "summary": _clip_expert_text(summary, 45),
            "fields": fields,
            "observations": observations,
            "uncertain": uncertain,
            "negative_constraints": _dedupe_prompt_items(negative),
            "confidence": confidence if fields or observations else 0.0,
            "raw": markdown,
            "format": "markdown",
        }
    )


