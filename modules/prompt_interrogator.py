def _expert_specs_for_stage(overview: dict[str, Any], stage: str = "full") -> list[dict[str, str]]:
    """Return the expert subset for a staged capability ladder."""
    spec_by_id = _expert_spec_by_id()
    normalized = str(stage or "full").strip().lower()
    if normalized in {"full", "staged", "expert", "all"}:
        return _expert_specs_for_overview(overview)

    has_person = _overview_has_person_subject(overview)
    if normalized in {"overview_plus_1", "plus_1", "one_expert"}:
        selected_ids = ["body_pose"] if has_person else ["materials_texture"]
    elif normalized in {"overview_plus_2_subject", "plus_2", "two_subject_experts"}:
        if has_person:
            second = "sexual_boundary" if _overview_mentions_boundary(overview) else "clothing_makeup"
            selected_ids = ["body_pose", second]
        else:
            selected_ids = ["composition", "materials_texture"]
    else:
        return _expert_specs_for_overview(overview)

    return [spec_by_id[expert_id] for expert_id in selected_ids if expert_id in spec_by_id]


