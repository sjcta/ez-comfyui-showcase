def _combine_expert_team_passes(global_json: dict[str, Any], subject_json: dict[str, Any]) -> dict[str, Any]:
    combined = copy.deepcopy(subject_json) if isinstance(subject_json, dict) else {}
    global_source = global_json if isinstance(global_json, dict) else {}
    if not isinstance(combined, dict):
        combined = {}
    for key in ("visual_evidence", "expert_plan"):
        if combined.get(key) in (None, "", [], {}) and global_source.get(key) not in (None, "", [], {}):
            combined[key] = copy.deepcopy(global_source.get(key))
    global_cards = global_source.get("fact_cards") if isinstance(global_source.get("fact_cards"), list) else []
    subject_cards = combined.get("fact_cards") if isinstance(combined.get("fact_cards"), list) else []
    if global_cards or subject_cards:
        cards: list[Any] = []
        seen_cards: set[str] = set()
        for card in [*global_cards, *subject_cards]:
            key = json.dumps(card, ensure_ascii=False, sort_keys=True) if isinstance(card, dict) else str(card)
            if key in seen_cards:
                continue
            seen_cards.add(key)
            cards.append(card)
        combined["fact_cards"] = cards
    global_obs = global_source.get("expert_observations") if isinstance(global_source.get("expert_observations"), dict) else {}
    subject_obs = combined.get("expert_observations") if isinstance(combined.get("expert_observations"), dict) else {}
    if global_obs or subject_obs:
        merged_obs = {**global_obs, **subject_obs}
        combined["expert_observations"] = merged_obs
    if combined.get("keyword_prompt") in (None, "") and global_source.get("keyword_prompt"):
        combined["keyword_prompt"] = global_source.get("keyword_prompt")
    global_neg = global_source.get("negative_prompt") if isinstance(global_source.get("negative_prompt"), list) else []
    subject_neg = combined.get("negative_prompt") if isinstance(combined.get("negative_prompt"), list) else []
    if global_neg or subject_neg:
        combined["negative_prompt"] = _dedupe_prompt_items(_iter_grouped_prompt_strings([*global_neg, *subject_neg]))
    return combined


