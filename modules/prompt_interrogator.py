def _expert_observations_from_batch_markdown(markdown: str, specs: list[dict[str, str]]) -> list[dict[str, Any]]:
    spec_by_id = {spec["id"]: spec for spec in specs}
    spec_by_label = {spec["label"]: spec for spec in specs}
    sections: dict[str, list[str]] = {spec["id"]: [] for spec in specs}
    current_id = ""
    for raw_line in str(markdown or "").splitlines():
        line = raw_line.strip()
        if line.startswith("#"):
            title = line.lstrip("#").strip()
            current_id = ""
            for label, spec in spec_by_label.items():
                if label and label in title:
                    current_id = spec["id"]
                    break
            if not current_id:
                for expert_id, spec in spec_by_id.items():
                    if expert_id and expert_id in title:
                        current_id = spec["id"]
                        break
            continue
        if current_id:
            sections[current_id].append(raw_line)
    results: list[dict[str, Any]] = []
    for spec in specs:
        section_text = "\n".join(sections.get(spec["id"]) or []).strip()
        if not section_text and len(specs) == 1:
            section_text = str(markdown or "").strip()
        results.append(_expert_observation_from_markdown(section_text, spec))
    return results


