def _grouped_prompt_richness(text: str) -> int:
    parts = [part.strip() for part in re.split(r"[，,；;、\n]+", str(text or "")) if part.strip()]
    return len(_dedupe_prompt_items(parts))


def _prompt_needs_spatial_render_legend(text: str) -> bool:
    return bool(SPATIAL_GRID_CODE_RE.search(str(text or "")))


def _replace_spatial_grid_codes(text: str) -> str:
    replaced = SPATIAL_GRID_CODE_RE.sub(lambda match: SPATIAL_GRID_CODE_MAP.get(match.group(0).lower(), match.group(0)), str(text or ""))
    for region in SPATIAL_GRID_CODE_MAP.values():
        replaced = replaced.replace(region + region, region)
    return replaced


def _strip_spatial_render_legend(text: str) -> str:
    cleaned = str(text or "").strip()
    if not cleaned:
        return ""
    cleaned = re.sub(
        r"九宫格坐标规则[:：][^，。；;]*?(?:12点向上、3点向右、6点向下、9点向左)?",
        "",
        cleaned,
    ).strip(" \t\r\n，,。.;；:：、")
    return _replace_spatial_grid_codes(cleaned)


def _looks_like_json_artifact_text(text: str) -> bool:
    cleaned = str(text or "").strip()
    if not cleaned:
        return False
    if cleaned.startswith("{") or cleaned.endswith("}"):
        return True
    return bool(JSON_ARTIFACT_RE.search(cleaned))


def _prepend_spatial_render_legend_if_needed(prompt: str) -> str:
    text = str(prompt or "").strip()
    if not text:
        return text
    return _replace_spatial_grid_codes(text)


