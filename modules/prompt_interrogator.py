def _iter_grouped_prompt_strings(value: Any) -> list[str]:
    if isinstance(value, dict):
        items: list[str] = []
        for nested in value.values():
            items.extend(_iter_grouped_prompt_strings(nested))
        return items
    if isinstance(value, (list, tuple)):
        items: list[str] = []
        for nested in value:
            items.extend(_iter_grouped_prompt_strings(nested))
        return items
    text = str(value or "").strip()
    return [text] if text else []


def _dedupe_prompt_items(values: list[str]) -> list[str]:
    items: list[str] = []
    keys: set[str] = set()
    for value in values:
        cleaned = str(value or "").strip(" \t\r\n，,。.;；:：、")
        if len(cleaned) < 2:
            continue
        key = re.sub(r"[\s，,。.;；:：、/|()（）【】\[\]{}\"'“”‘’]+", "", cleaned).lower()
        if not key or key in keys:
            continue
        keys.add(key)
        items.append(cleaned)
    return items


def _dedupe_similar_prompt_items(values: list[str], *, ratio: float = 0.86) -> list[str]:
    items: list[str] = []
    keys: list[str] = []
    for value in _dedupe_prompt_items(values):
        key = re.sub(r"[\s，,。.;；:：、/|()（）【】\[\]{}\"'“”‘’]+", "", str(value or "")).lower()
        if any(key and (key in old or old in key or difflib.SequenceMatcher(None, key, old).ratio() >= ratio) for old in keys):
            continue
        keys.append(key)
        items.append(value)
    return items


