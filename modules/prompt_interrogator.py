def _strip_prompt_metadata_key_prefix(text: str) -> str:
    cleaned = str(text or "").strip()
    previous = None
    while previous != cleaned:
        previous = cleaned
        cleaned = PROMPT_METADATA_KEY_PREFIX_RE.sub("", cleaned).strip()
    return cleaned


